package pp;

import java.util.List;

public class Students {
	private List<String> namee;
	 private List<String> emaill;
	 private List<String> numberr;
	 private List<String> addresss;
	public List<String> getNamee() {
		return namee;
	}
	public void setNamee(List<String> namee) {
		this.namee = namee;
	}
	public List<String> getEmaill() {
		return emaill;
	}
	public void setEmaill(List<String> emaill) {
		this.emaill = emaill;
	}
	public List<String> getNumberr() {
		return numberr;
	}
	public void setNumberr(List<String> numberr) {
		this.numberr = numberr;
	}
	public List<String> getAddresss() {
		return addresss;
	}
	public void setAddresss(List<String> addresss) {
		this.addresss = addresss;
	}
	
	 
	
}
